u8 NuzlockeFlagSet(u16 mapsec)
{
    u8 id = NuzlockeLUT[mapsec];
    u8 * ptr = &gSaveBlock1Ptr->NuzlockeEncounterFlags[id / 8];
    if (ptr)
        * ptr |= 1 << (id & 7);
    return 0;
}

u8 NuzlockeFlagClear(u16 mapsec)
{
    u8 id = NuzlockeLUT[mapsec];
    u8 * ptr = &gSaveBlock1Ptr->NuzlockeEncounterFlags[id / 8];
    if (ptr)
        * ptr &= ~(1 << (id & 7));
    return 0;
}

u8 NuzlockeFlagGet(u16 mapsec)
{
    u8 id = NuzlockeLUT[mapsec];
    u8 * ptr = &gSaveBlock1Ptr->NuzlockeEncounterFlags[id / 8];

    if (!ptr)
        return 0;

    if (!(((*ptr) >> (id & 7)) & 1))
        return 0;

    return 1;
}

